// Hide & Show using jQuery
$("#btn1").click(function(){
    $("#para").hide();
});


$("#btn2").click(function(){
    $("#para").show();
});


$("#btn3").click(function(){
    $("#para").toggle();
});


// SlidUp & SlideDown
$("#btn4").click(function(){
    $("#para").slideUp();
});


$("#btn5").click(function(){
    $("#para").slideDown();
});


$("#btn6").click(function(){
    $("#para").slideToggle();
});